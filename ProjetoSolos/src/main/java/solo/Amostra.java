package solo;

/**
 *
 * @author vande
 */
public class Amostra {

    private String texturaSolo = "";
    private String tipo = "";
    private int profundidadde = 0;
    private String sistemaCultivo = "";

    /**
     * @return the texturaSolo
     */
    public String getTexturaSolo() {
        return texturaSolo;
    }

    /**
     * @param texturaSolo the texturaSolo to set
     */
    public void setTexturaSolo(String texturaSolo) {
        this.texturaSolo = texturaSolo;
    }

    /**
     * @return the tipo
     */
    public String getTipo() {
        return tipo;
    }

    /**
     * @param tipo the tipo to set
     */
    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    /**
     * @return the profundidadde
     */
    public int getProfundidadde() {
        return profundidadde;
    }

    /**
     * @param profundidadde the profundidadde to set
     */
    public void setProfundidadde(int profundidadde) {
        this.profundidadde = profundidadde;
    }

    /**
     * @return the sistemaCultivo
     */
    public String getSistemaCultivo() {
        return sistemaCultivo;
    }

    /**
     * @param sistemaCultivo the sistemaCultivo to set
     */
    public void setSistemaCultivo(String sistemaCultivo) {
        this.sistemaCultivo = sistemaCultivo;
    }
}
